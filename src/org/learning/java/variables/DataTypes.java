package org.learning.java.variables;

public class DataTypes {

  public static void main(String[] args) {
    System.out.println("Testo1");
    System.out.println("Testo2");

    System.out.print("Testo3\t");
    System.out.print("Testo4\n");

    double x = 5;
    double y = 10;

    System.out.println(x);
    System.out.println(y);
    System.out.println(x / y);

    int n1 = 5;
    int n2 = 10;
    System.out.println(n1 / n2);

    double d = 0.1;
    System.out.println(d + d + d + d + d + d + d + d);

    System.out.println(Math.max(10.5, 15.9));
  }

}
